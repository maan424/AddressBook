using System;

class Address
{
    public string name;
    public string address;
    public string email;
    public string telephone;


    public Address(string firstname,string lastname, string address, string email, string telephone)
    {
        this.name = firstname;
        this.name = lastname;
        this.address = address;
        this.email = email;
        this.telephone = telephone;
    }
}
